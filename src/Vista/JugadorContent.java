     public void listar(){
        DefaultTableModel modelTable = (DefaultTableModel) Tjugador.getModel();
        modelTable.getDataVector().removeAllElements();
        Tjugador.updateUI();

        List<Jugador> js = SqlJugador.listar();

        for (Jugador p : js) {
            String[] data = new String[]{p.getIdentificacion(),p.getNombre(),p.getApellido(),p.getFecha_nacimiento().toString(),p.getPosicion(),Integer.toString(p.getCodigoEquipo()),p.getNombreEquipo()};
            modelTable.addRow(data);
        }
          
    }

