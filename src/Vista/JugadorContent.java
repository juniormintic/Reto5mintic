    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        Jugador p=new Jugador(txtidentificacion.getText());
        p.setNombre(txtnombre.getText());
        p.setApellido(txtapellido.getText());
        p.setFecha_nacimiento(txtfechanacimiento.getText());
        p.setPosicion(txtposicion.getText());
        p.setCodigoEquipo(Integer.parseInt(txtcodigoequipo.getText()));
        
        boolean res =SqlJugador.registrar(p);
        if(res){
            JOptionPane.showMessageDialog(null,"Se guardo el registro con exito");
             this.btnlimpiarActionPerformed(evt);
             listar();
        }else{
            JOptionPane.showMessageDialog(null,"No se pudo guardar el registro");
        
        }
        
        
        
    }//GEN-LAST:event_btnguardarActionPerformed

