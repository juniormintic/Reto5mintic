    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
        Jugador j=new Jugador(txtbuscar.getText());
        Jugador res =SqlJugador.consulta(j);
        if(res!=null){
        txtidentificacion.setText(res.getIdentificacion());
        txtnombre.setText(res.getNombre());
        txtapellido.setText(res.getApellido());
        txtfechanacimiento.setText(res.getFecha_nacimiento().toString());   
        txtposicion.setText(res.getPosicion());
        txtcodigoequipo.setText(Integer.toString(res.getCodigoEquipo()));
        }else{
            JOptionPane.showMessageDialog(null,"no se encontro el registro");
        }
        
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        Jugador j=new Jugador(txtbuscar.getText());
        boolean res =SqlJugador.eliminar(j);
        if(res){
             JOptionPane.showMessageDialog(null,"Se elimino el registro");
             this.btnlimpiarActionPerformed(evt);
             listar();
        }else{
            JOptionPane.showMessageDialog(null,"no se encontro el registro");
        }
    }//GEN-LAST:event_btneliminarActionPerformed

