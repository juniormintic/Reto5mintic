    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:       
        PartidosEquipo pe=new PartidosEquipo(Integer.parseInt(txtbuscar.getText()),Integer.parseInt(txtbuscar1.getText()),false);
        PartidosEquipo res= SqlPartidosEquipo.consulta(pe);
        if(res!=null){
            txtcodigopartido.setText(Integer.toString(res.getPartidoCodigo()));
            txtcodigoequipo.setText(Integer.toString(res.getEquipoCodigo()));
            txteslocal.setText(res.getEsLocal()?"Local":"Visitante");            
            
        }else{
             JOptionPane.showMessageDialog(null, "No se encontro ningun registro");
        
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

