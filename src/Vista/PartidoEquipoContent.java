      public void listar(){
       DefaultTableModel modelTable = (DefaultTableModel) Tpartidoequipo.getModel();
        modelTable.getDataVector().removeAllElements();
        Tpartidoequipo.updateUI();
        List< PartidosEquipo>dt=SqlPartidosEquipo.listar();       
        
        for(PartidosEquipo p:dt){
            String[] data =new String[3];
            data[0]=Integer.toString(p.getPartidoCodigo());
            data[1]=Integer.toString(p.getEquipoCodigo());
            data[2]=p.getEsLocal()?"Local":"Visitante";
            modelTable.addRow(data);
        }
    }
