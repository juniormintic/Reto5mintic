    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
        String buscar= txtbuscar.getText();
        DirectorTecnico p =new DirectorTecnico(buscar);
        DirectorTecnico res=SqlDirectorTecnico.consulta(p);
        if(res!=null){
        txtidentificacion.setText(res.getIdentificacion());
        txtnombre.setText(res.getNombre());
        txtapellido.setText(res.getApellido());
        txtfechanacimiento.setText(res.getFecha_nacimiento().toString());   
        txtmescontrato.setText(Integer.toString(res.getMesContrato()));
        txtaniocontrato.setText(Integer.toString(res.getAnioContrato()));
        
        }else{
            JOptionPane.showMessageDialog(null,"El director tecnico no existe");
        }
        
        
    }//GEN-LAST:event_btnbuscarActionPerformed

