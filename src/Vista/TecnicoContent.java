    public void listar(){
       DefaultTableModel modelTable = (DefaultTableModel) Ttecnico.getModel();
        modelTable.getDataVector().removeAllElements();
        Ttecnico.updateUI();
        List< DirectorTecnico >dt=SqlDirectorTecnico.listar();        
        for(DirectorTecnico p:dt){
            String[] data =new String[]{p.getIdentificacion(),p.getNombre(),p.getApellido(),p.getFecha_nacimiento().toString(),Integer.toString(p.getMesContrato()),Integer.toString(p.getAnioContrato())};
            modelTable.addRow(data);
        }
    }
